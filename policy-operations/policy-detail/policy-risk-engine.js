export function calcularRiesgoPoliza({

    daysWithoutContact = 0,

    pendingPayments = 0,

    renewalClose = false

}) {

    let score = 0;

    /*
    |--------------------------------------------------------------------------
    | Contact inactivity
    |--------------------------------------------------------------------------
    */

    if (daysWithoutContact > 30) {

        score += 40;
    }

    /*
    |--------------------------------------------------------------------------
    | Pending payments
    |--------------------------------------------------------------------------
    */

    score += (
        pendingPayments
        * 20
    );

    /*
    |--------------------------------------------------------------------------
    | Renewal
    |--------------------------------------------------------------------------
    */

    if (renewalClose) {

        score += 30;
    }

    /*
    |--------------------------------------------------------------------------
    | Final
    |--------------------------------------------------------------------------
    */

    let level = 'LOW';

    if (score >= 70) {

        level = 'HIGH';

    } else if (score >= 40) {

        level = 'MEDIUM';
    }

    return {

        score,

        level
    };
}