export function filtrarPolizas({

    polizas = [],

    status = '',

    producto = ''

}) {

    return polizas.filter(

        poliza => {

            /*
            |--------------------------------------------------------------------------
            | Status
            |--------------------------------------------------------------------------
            */

            if (

                status

                &&

                poliza.status
                !== status
            ) {

                return false;
            }

            /*
            |--------------------------------------------------------------------------
            | Producto
            |--------------------------------------------------------------------------
            */

            if (

                producto

                &&

                poliza.producto
                !== producto
            ) {

                return false;
            }

            return true;
        }
    );
}