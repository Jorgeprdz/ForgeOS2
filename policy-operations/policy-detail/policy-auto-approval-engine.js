/*
|--------------------------------------------------------------------------
| MODULE: policy-auto-approval-engine.js
|--------------------------------------------------------------------------
|
| Auto-aprobación inteligente.
|
|--------------------------------------------------------------------------
*/

export function puedeAutoAprobar({

    confidenceMap = {}

}) {

    const values =

        Object.values(
            confidenceMap
        );

    /*
    |--------------------------------------------------------------------------
    | Confidence promedio
    |--------------------------------------------------------------------------
    */

    const average =

        values.reduce(

            (acc, value) =>

                acc + value,

            0
        )

        / values.length;

    /*
    |--------------------------------------------------------------------------
    | Threshold auto-approve
    |--------------------------------------------------------------------------
    */

    return average >= 95;
}