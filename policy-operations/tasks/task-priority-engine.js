/*
|--------------------------------------------------------------------------
| MODULE: task-priority-engine.js
|--------------------------------------------------------------------------
|
| Dynamic task priority engine.
|
|--------------------------------------------------------------------------
*/

export function calcularPrioridadTask({

    riskLevel = 'LOW',

    renewalClose = false,

    overdueDays = 0

}) {

    /*
    |--------------------------------------------------------------------------
    | Critical
    |--------------------------------------------------------------------------
    */

    if (

        riskLevel === 'HIGH'
        ||

        overdueDays > 7
    ) {

        return 'CRITICAL';
    }

    /*
    |--------------------------------------------------------------------------
    | High
    |--------------------------------------------------------------------------
    */

    if (renewalClose) {

        return 'HIGH';
    }

    /*
    |--------------------------------------------------------------------------
    | Default
    |--------------------------------------------------------------------------
    */

    return 'MEDIUM';
}