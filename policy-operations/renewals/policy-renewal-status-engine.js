/*
|--------------------------------------------------------------------------
| MODULE: policy-renewal-status-engine.js
|--------------------------------------------------------------------------
|
| Policy renewal visual status.
|
|--------------------------------------------------------------------------
*/

export function obtenerEstadoRenovacion({

    renewalDate

}) {

    const now =
        new Date();

    const renewal =
        new Date(renewalDate);

    const days = Math.ceil(

        (
            renewal - now
        )

        /

        (
            1000
            * 60
            * 60
            * 24
        )
    );

    /*
    |--------------------------------------------------------------------------
    | Critical
    |--------------------------------------------------------------------------
    */

    if (days <= 15) {

        return {

            status:
                'CRITICAL',

            days
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Warning
    |--------------------------------------------------------------------------
    */

    if (days <= 45) {

        return {

            status:
                'WARNING',

            days
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Stable
    |--------------------------------------------------------------------------
    */

    return {

        status:
            'STABLE',

        days
    };
}