export async function procesarArchivoImportacion({

    file

}) {

    /*
    |--------------------------------------------------------------------------
    | Validar archivo
    |--------------------------------------------------------------------------
    */

    if (!file) {

        throw new Error(
            'Archivo requerido'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | Detectar tipo
    |--------------------------------------------------------------------------
    */

    const tipo = detectarTipoArchivo(
        file
    );

    return {

        tipo,

        nombre:
            file.name,

        size:
            file.size,

        status:
            'procesando'
    };
}

function detectarTipoArchivo(

    file

) {

    const mime = file.type;

    /*
    |--------------------------------------------------------------------------
    | Excel
    |--------------------------------------------------------------------------
    */

    if (

        mime.includes(
            'spreadsheet'
        )

        ||

        mime.includes(
            'excel'
        )
    ) {

        return 'excel';
    }

    /*
    |--------------------------------------------------------------------------
    | CSV
    |--------------------------------------------------------------------------
    */

    if (

        mime.includes(
            'csv'
        )
    ) {

        return 'csv';
    }

    /*
    |--------------------------------------------------------------------------
    | PDF
    |--------------------------------------------------------------------------
    */

    if (

        mime.includes(
            'pdf'
        )
    ) {

        return 'pdf';
    }

    /*
    |--------------------------------------------------------------------------
    | Imagen
    |--------------------------------------------------------------------------
    */

    if (

        mime.includes(
            'image'
        )
    ) {

        return 'image';
    }

    return 'unknown';
}