/*
|--------------------------------------------------------------------------
| MODULE: policy-human-review-engine.js
|--------------------------------------------------------------------------
|
| Determina revisión humana.
|
|--------------------------------------------------------------------------
*/

export function requiereRevisionHumana({

    validation = {},

    reviewFields = []

}) {

    /*
    |--------------------------------------------------------------------------
    | Errores validación
    |--------------------------------------------------------------------------
    */

    if (

        validation.errors
        &&

        validation.errors.length
    ) {

        return true;
    }

    /*
    |--------------------------------------------------------------------------
    | Campos dudosos
    |--------------------------------------------------------------------------
    */

    if (

        reviewFields.length
        >= 1
    ) {

        return true;
    }

    return false;
}