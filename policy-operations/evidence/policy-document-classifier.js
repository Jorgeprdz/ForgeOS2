/*
|--------------------------------------------------------------------------
| MODULE: policy-document-classifier.js
|--------------------------------------------------------------------------
|
| Clasificación inteligente de documentos.
|
|--------------------------------------------------------------------------
*/

export function clasificarDocumento({

    text = ''

}) {

    const normalized =

        text.toUpperCase();

    /*
    |--------------------------------------------------------------------------
    | Carátula póliza
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'PÓLIZA'
        )

        ||

        normalized.includes(
            'POLIZA'
        )
    ) {

        return 'policy';
    }

    /*
    |--------------------------------------------------------------------------
    | Recibo
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'RECIBO'
        )
    ) {

        return 'receipt';
    }

    /*
    |--------------------------------------------------------------------------
    | Endoso
    |--------------------------------------------------------------------------
    */

    if (

        normalized.includes(
            'ENDOSO'
        )
    ) {

        return 'endorsement';
    }

    return 'unknown';
}