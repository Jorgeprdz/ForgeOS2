/*
|--------------------------------------------------------------------------
| MODULE: command-parser-engine.js
|--------------------------------------------------------------------------
|
| Command parser engine.
|
|--------------------------------------------------------------------------
*/

export function parsearComando({

    input = ''

}) {

    /*
    |--------------------------------------------------------------------------
    | Slash command
    |--------------------------------------------------------------------------
    */

    if (

        input.startsWith('/')
    ) {

        return {

            type:
                'COMMAND',

            value:
                input.slice(1)
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Entity command
    |--------------------------------------------------------------------------
    */

    if (

        input.startsWith('@')
    ) {

        return {

            type:
                'ENTITY',

            value:
                input.slice(1)
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Natural language
    |--------------------------------------------------------------------------
    */

    return {

        type:
            'TEXT',

        value:
            input
    };
}