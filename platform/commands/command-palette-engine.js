/*
|--------------------------------------------------------------------------
| MODULE: command-palette-engine.js
|--------------------------------------------------------------------------
|
| Universal command palette.
|
|--------------------------------------------------------------------------
*/

export function parseCommandPalette({

    query = ''

}) {

    /*
    |--------------------------------------------------------------------------
    | Quick action
    |--------------------------------------------------------------------------
    */

    if (

        query.startsWith('/')
    ) {

        return {

            type: 'ACTION',

            value:

                query
                .replace('/', '')
                .trim()
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Entity search
    |--------------------------------------------------------------------------
    */

    if (

        query.startsWith('@')
    ) {

        return {

            type: 'ENTITY',

            value:

                query
                .replace('@', '')
                .trim()
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Standard search
    |--------------------------------------------------------------------------
    */

    return {

        type: 'SEARCH',

        value:
            query
    };
}