/*
|--------------------------------------------------------------------------
| MODULE: referral-temperature-engine.js
|--------------------------------------------------------------------------
|
| Temperatura inteligente de referidos.
|
|--------------------------------------------------------------------------
*/

export function calcularTemperatura({

    diasSinActividad = 0,

    tieneCita = false,

    respondio = false

}) {

    /*
    |--------------------------------------------------------------------------
    | Hot
    |--------------------------------------------------------------------------
    */

    if (
        tieneCita
        && respondio
    ) {

        return 'hot';
    }

    /*
    |--------------------------------------------------------------------------
    | Warm
    |--------------------------------------------------------------------------
    */

    if (
        respondio
    ) {

        return 'warm';
    }

    /*
    |--------------------------------------------------------------------------
    | Cold
    |--------------------------------------------------------------------------
    */

    if (
        diasSinActividad >= 7
    ) {

        return 'cold';
    }

    return 'warm';
}